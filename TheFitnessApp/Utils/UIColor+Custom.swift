import UIKit

extension UIColor {
    class var dimmedBlue: UIColor {
        return UIColor(red: 36/255, green: 52/255, blue: 65/255, alpha: 1.0)
    }
    class var lightDimmedBlue: UIColor {
        return UIColor(red: 57/255, green: 87/255, blue: 112/255, alpha: 1.0)
    }
    
    class var lightShadow: UIColor {
        return UIColor(red: 56/255, green: 82/255, blue: 104/255, alpha: 1.0)
    }
    
    class var darkShadow: UIColor {
        return UIColor(red: 13/255, green: 19/255, blue: 24/255, alpha: 1.0)
    }
    
    class var customRed: UIColor {
        return UIColor(red: 204/255, green: 44/255, blue: 44/255, alpha: 1.0)
    }
    
    class var customWhite: UIColor {
        return UIColor(red: 239/255, green: 239/255, blue: 239/255, alpha: 1.0)
    }
}





































